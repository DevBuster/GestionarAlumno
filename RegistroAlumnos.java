    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int mensaje = JOptionPane.showConfirmDialog(this, "¿SEGURO(A) QUE QUIERE ELIMINAR EL REGISTRO?", "CONFIRMAR PROCESO", JOptionPane.INFORMATION_MESSAGE);

        switch (mensaje) {
            case JOptionPane.YES_OPTION -> {
                try {
                    if (tblAlumno.getSelectedRowCount() != 0) {
                        int seleccionarFila = tblAlumno.getSelectedRow();
                        tablaModelo.removeRow(seleccionarFila);

                        listaAlumnos.remove(seleccionarFila);

                        refrescarTabla();
                        btnBorrarActionPerformed(evt);

                        JOptionPane.showMessageDialog(this, "REGISTRO ELIMINADO", "INFORMACIÓN", JOptionPane.INFORMATION_MESSAGE);
                    } else {
                        JOptionPane.showMessageDialog(this, "Debe seleccionar una fila para eliminar el registro", "INFORMACIÓN", JOptionPane.INFORMATION_MESSAGE);
                    }
                } catch (HeadlessException e) {
                    JOptionPane.showMessageDialog(this, "ERROR AL ELIMINAR El REGISTRO\n Tipo de error: " + e, "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }
            case JOptionPane.NO_OPTION ->
                JOptionPane.showMessageDialog(this, "PROCESO DENEGADO", "INFORMACIÓN", JOptionPane.ERROR_MESSAGE);
            default ->
                JOptionPane.showMessageDialog(this, "PROCESO CANCELADO", "INFORMACIÓN", JOptionPane.CANCEL_OPTION);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEliminarTodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarTodoActionPerformed
        int filas = tblAlumno.getRowCount();

        if (filas != 0) {
            int mensaje = JOptionPane.showConfirmDialog(this, "¿SEGURO(A) QUE QUIERE ELIMINAR TODO EL REGISTRO?", "CONFIRMAR PROCESO", JOptionPane.INFORMATION_MESSAGE);

            switch (mensaje) {
                case JOptionPane.YES_OPTION -> {
                    try {

                        for (int i = filas - 1; i >= 0; i--) {
                            tablaModelo.removeRow(i);
                            listaAlumnos.remove(i);
                        }

                        refrescarTabla();
                        btnBorrarActionPerformed(evt);

                        JOptionPane.showMessageDialog(this, "REGISTROS ELIMINADOS", "INFORMACIÓN", JOptionPane.INFORMATION_MESSAGE);
                    } catch (HeadlessException e) {
                        JOptionPane.showMessageDialog(this, "ERROR AL ELIMINAR TODOS REGISTRO\n Tipo de error: " + e, "ERROR", JOptionPane.ERROR_MESSAGE);
                    }
                }
                case JOptionPane.NO_OPTION ->
                    JOptionPane.showMessageDialog(this, "PROCESO DENEGADO", "INFORMACIÓN", JOptionPane.ERROR_MESSAGE);
                default ->
                    JOptionPane.showMessageDialog(this, "PROCESO CANCELADO", "INFORMACIÓN", JOptionPane.CANCEL_OPTION);
            }
        } else {
            JOptionPane.showMessageDialog(this, "NO HAY REGISTROS POR ELIMINAR", "INFORMACIÓN", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarTodoActionPerformed

