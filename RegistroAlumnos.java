    public final void refrescarTabla() {
        /*
        * ELIMINA LAS FILAS CADA VEZ QUE SE LLAME AL METODO PARA EVITAR DATOS
        * DUPLICADOS
         */
        while (tablaModelo.getRowCount() > 0) {
            tablaModelo.removeRow(0);
        }

        /*
        * RECORREMOS EL OBJETO Y LO ALMACENAMOS EN UN VECTOR DE 8 POSICIONES,
        * 1 POSICION POR CADA VARIABLE O CAMPO QUE TENGAMOS EN EL MODELO ALUMNO
        * NOTA: TIPO DE DATO OBJECT PARA ALMACENAR CUALQUIER TIPO DE DATO
         */
        for (Alumno listaAlumno : listaAlumnos) {
            Object objetoAlumno[] = new Object[8];
            objetoAlumno[0] = listaAlumno.getNombre();
            objetoAlumno[1] = listaAlumno.getApellido();
            objetoAlumno[2] = listaAlumno.getEdad();
            objetoAlumno[3] = listaAlumno.getIdentificacion();
            objetoAlumno[4] = listaAlumno.getCarrera();
            objetoAlumno[5] = listaAlumno.getGrupo();
            objetoAlumno[6] = listaAlumno.getSemestre();
            objetoAlumno[7] = listaAlumno.getPromedio();

            tablaModelo.addRow(objetoAlumno);
        }
        tblAlumno.setModel(tablaModelo);
    }

