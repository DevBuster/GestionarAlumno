    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int mensaje = JOptionPane.showConfirmDialog(this, "¿SEGURO(A) QUE QUIERE EDITAR EL REGISTRO?", "CONFIRMAR PROCESO", JOptionPane.INFORMATION_MESSAGE);

        switch (mensaje) {
            case JOptionPane.YES_OPTION -> {
                try {
                    Alumno objetoAlumno = new Alumno();
                    objetoAlumno.setNombre(txtNombre.getText());
                    objetoAlumno.setApellido(txtApellido.getText());
                    objetoAlumno.setEdad(Integer.parseInt(spnEdad.getValue().toString()));
                    objetoAlumno.setIdentificacion(Integer.parseInt(txtIdentificacion.getText()));
                    objetoAlumno.setCarrera(cmbCarrera.getSelectedItem().toString());
                    objetoAlumno.setGrupo(Integer.parseInt(cmbGrupo.getSelectedItem().toString()));
                    objetoAlumno.setSemestre(cmbSemestre.getSelectedItem().toString());
                    objetoAlumno.setPromedio(Double.parseDouble(spnPromedio.getValue().toString()));

                    int fila = tblAlumno.getSelectedRow();

                    listaAlumnos.set(fila, objetoAlumno);

                    refrescarTabla();
                    btnBorrarActionPerformed(evt);

                    JOptionPane.showMessageDialog(this, "REGISTRO EDITADO", "INFORMACIÓN", JOptionPane.INFORMATION_MESSAGE);
                } catch (HeadlessException | NumberFormatException e) {
                    JOptionPane.showMessageDialog(this, "ERROR AL EDITAR EL REGISTRO\n Tipo de error: " + e, "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            }
            case JOptionPane.NO_OPTION ->
                JOptionPane.showMessageDialog(this, "PROCESO DENEGADO", "INFORMACIÓN", JOptionPane.ERROR_MESSAGE);
            default ->
                JOptionPane.showMessageDialog(this, "PROCESO CANCELADO", "INFORMACIÓN", JOptionPane.CANCEL_OPTION);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

